    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        try {

            String ten = txtten.getText().trim();
            String imei = txtImei.getText().trim();
            QlSanPham qlSanPham = new QlSanPham(imei, ten);
            String text = this.sanPhamServiceImpl.save(qlSanPham);
            JOptionPane.showMessageDialog(rootPane, text);
            loadDataOnTable();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "bạn phải nhập số");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int row = tblsanpham.getSelectedRow();
        if (row == - 1) {
            JOptionPane.showMessageDialog(rootPane, "Bạn phải chọn 1 dòng tương ứg");
            return;
        }
        String id = tableModel.getValueAt(row, 0).toString();
        QlSanPham qLRam = this.sanPhamServiceImpl.findOne(UUID.fromString(id));
        String text = this.sanPhamServiceImpl.delete(qLRam);
        JOptionPane.showMessageDialog(rootPane, text);
        loadDataOnTable();
    }//GEN-LAST:event_btnxoaActionPerformed

