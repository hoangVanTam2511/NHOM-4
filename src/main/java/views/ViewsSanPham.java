    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int row = tblsanpham.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(rootPane, "bạn phải chọn 1 dòng tương ứng");
            return;
        }
        String id = txtid.getText();
        QlSanPham qlSanPham = new QlSanPham();
        qlSanPham = this.sanPhamServiceImpl.findOne(UUID.fromString(id));

        qlSanPham.setSoImei(txtImei.getText());
        qlSanPham.setTen(txtten.getText());

        String text = this.sanPhamServiceImpl.update(qlSanPham);
        JOptionPane.showMessageDialog(this, text);

        loadDataOnTable();
    }//GEN-LAST:event_btnsuaActionPerformed

